async def add_note(
    session: AsyncSession, case_id: UUID, payload: CaseNoteCreate, author_id: UUID
) -> CaseNoteRead:
    """Add a note to a case."""
    await _get_case_or_404(session, case_id)

    note = CaseNote(
        case_id=case_id,
        author_id=author_id,
        content=payload.content,
        is_private=payload.is_internal,
    )
    session.add(note)
    await session.flush()
    await session.refresh(note)

    author_name = note.author.full_name if note.author else None

    return CaseNoteRead(
        id=note.id,
        case_id=note.case_id,
        author_id=note.author_id,
        author_name=author_name,
        content=note.content,
        is_internal=note.is_private,
        created_at=note.created_at,
        updated_at=note.updated_at,
    )


async def list_notes(
    session: AsyncSession, case_id: UUID, pagination: PaginationParams
) -> PaginatedResponse[CaseNoteRead]:
    """List notes for a case."""
    await _get_case_or_404(session, case_id)
    query = (
        select(CaseNote)
        .where(CaseNote.case_id == case_id)
        .order_by(CaseNote.created_at.desc())
    )
    return await paginate(session, query, pagination, CaseNoteRead)


async def get_history(
    session: AsyncSession, case_id: UUID, pagination: PaginationParams
) -> PaginatedResponse[CaseHistoryRead]:
    """Get the audit history for a case."""
    await _get_case_or_404(session, case_id)
    query = (
        select(CaseHistory)
        .where(CaseHistory.case_id == case_id)
        .order_by(CaseHistory.created_at.desc())
    )
    return await paginate(session, query, pagination, CaseHistoryRead)
